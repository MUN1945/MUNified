async function main() {
  console.log('Seeding assessment questions...');
  console.log(`Total questions to seed: ${questions.length}`);

  // Count by tier
  const tierCounts: Record<string, number> = {};
  for (const q of questions) {
    tierCounts[q.tier] = (tierCounts[q.tier] || 0) + 1;
  }
  console.log('\nQuestions per tier:');
  for (const [tier, count] of Object.entries(tierCounts)) {
    console.log(`  ${tier}: ${count}`);
  }

  // Count by category
  const catCounts: Record<string, number> = {};
  for (const q of questions) {
    catCounts[q.category] = (catCounts[q.category] || 0) + 1;
  }
  console.log('\nQuestions per category:');
  for (const [cat, count] of Object.entries(catCounts)) {
    console.log(`  ${cat}: ${count}`);
  }

  // Count by question type
  const typeCounts: Record<string, number> = {};
  for (const q of questions) {
    typeCounts[q.questionType] = (typeCounts[q.questionType] || 0) + 1;
  }
  console.log('\nQuestions per type:');
  for (const [type, count] of Object.entries(typeCounts)) {
    console.log(`  ${type}: ${count}`);
  }

  // Clear existing questions
  const deleteResult = await prisma.assessmentQuestion.deleteMany({});
  console.log(`\nDeleted ${deleteResult.count} existing questions`);

  // Insert questions in batches
  const batchSize = 20;
  let inserted = 0;

  for (let i = 0; i < questions.length; i += batchSize) {
    const batch = questions.slice(i, i + batchSize);
    const promises = batch.map(q =>
      prisma.assessmentQuestion.create({
        data: {
          tier: q.tier,
          questionType: q.questionType,
          category: q.category,
          text: q.text,
          options: JSON.stringify(q.options),
          correctAnswer: q.correctAnswer,
          explanation: q.explanation,
          difficulty: q.difficulty,
          points: q.points,
          isActive: true,
        },
      })
    );

    const results = await Promise.all(promises);
    inserted += results.length;
    console.log(`Inserted batch: ${inserted}/${questions.length}`);
  }

  console.log(`\n✅ Successfully seeded ${inserted} assessment questions!`);

  // Verify
  const totalInDb = await prisma.assessmentQuestion.count();
  console.log(`Verification: ${totalInDb} questions in database`);
}

main()
  .catch((e) => {
    console.error('Seed failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
